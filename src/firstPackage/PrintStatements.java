package firstPackage;

public class PrintStatements {
  public static void main(String[] args) {
    // Print, Printf, Println
    System.out.print("Hello");
    System.out.print("How are you"); // print prints data in the same line, it ends your cursor position to where it ends
    // not to a new line. 

    System.out.println("Hello"); // moves to a new line, next print happens in a new line
    System.out.printf("How are you"); 
  }
}
